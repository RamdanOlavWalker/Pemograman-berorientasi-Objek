package tabunganpelajar;

public class Orang {
    String NamaOrang;
    String AlamatOrang;
    String NoKTPOrang;
    
    void setNamaOrang(String Nama)
    {   this.NamaOrang = Nama;      }
    String getNamaOrang()
    {   return NamaOrang;           }
    
    void setAlamatOrang(String Nama)
    {   this.AlamatOrang = Nama;      }
    String getAlamatOrang()
    {   return AlamatOrang;           }
    
    void setNoKTPOrang(String Nama)
    {   this.NoKTPOrang = Nama;      }
    String getNoKTPOrang()
    {   return NoKTPOrang;           }
}
